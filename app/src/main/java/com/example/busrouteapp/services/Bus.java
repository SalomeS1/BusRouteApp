package com.example.busrouteapp.services;


public class Bus {


    private String busNumber;
    private String arrivalTimeLeft;

    public String getBusNumber() {
        return busNumber;
    }

    public void setBusNumber(String busNumber) {
        this.busNumber = busNumber;
    }

    public String getArrivalTimeLeft() {
        return arrivalTimeLeft;
    }

    public void setArrivalTimeLeft(String arrivalTimeLeft) {
        this.arrivalTimeLeft = arrivalTimeLeft;
    }


}
